package com.pesandar.schoolmanagement.model.entities;

public class SchoolManagement {
	private String schoolfounder;
	private String schoolname;
	private String schooladdress;
	private String schoolphno;
	public String getSchoolfounder() {
		return schoolfounder;
	}
	public void setSchoolfounder(String schoolfounder) {
		this.schoolfounder = schoolfounder;
	}
	public String getSchoolname() {
		return schoolname;
	}
	public void setSchoolname(String schoolname) {
		this.schoolname = schoolname;
	}
	public String getSchooladdress() {
		return schooladdress;
	}
	public void setSchooladdress(String schooladdress) {
		this.schooladdress = schooladdress;
	}
	public String getSchoolphno() {
		return schoolphno;
	}
	public void setSchoolphno(String schoolphno) {
		this.schoolphno = schoolphno;
	}
	
	
	
	
}
